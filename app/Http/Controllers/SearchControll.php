     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
      $alldata=Place::orderBy('hotelName');
      $keyword=$request->input('keyword');

      if(!empty($keyword)){
        $alldata->where('hotelName','LIKE','%'.$keyword.'%');
      }

      $alldata=$alldata->paginate(5);
      
      $place=$alldata;
      return view('place.index',compact('place',$place));
    }

